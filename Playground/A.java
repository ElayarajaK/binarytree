class A
{

 //

       /*
            Arithmetic operators

            addition =       (+)
            Subtraction =    (-)
            Multiplication = (*)
            Division       = (/)
            Reminder       = (%)
       */
   public static void main(String []args)
    {

        

        int i =10;
        int j =10;



        System.out.println(i + j );
        System.out.println(i - j );
        System.out.println(i * j );
        System.out.println(i / j );
        System.out.println(i % j );


        System.out.println(10 + 10 );
        System.out.println(10 - 10 );
        System.out.println(10 * 10 );
        System.out.println(10 / 10 );
        System.out.println(10 % 10 );



         


        
    }

}